#include<iostream>
#include<cmath>
using namespace std;

int main()
{
 cout << "Square root of 16: " << sqrt(16) << endl;
 cout << "Absolute value of -5: " << abs(-5) << endl;
 cout << "2 to the power of 3: " << pow(2, 3) << endl;
 cout << "Ceiling of 3.2: " << ceil(3.2) << endl;
 cout << "Floor of 3.8: " << floor(3.8) << endl;
 
 return 0;
}